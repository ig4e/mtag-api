export function isVideoFile(fileName: string) {
	const videoExtensions = [
		".3g2",
		".3gp",
		".amv",
		".asf",
		".avi",
		".drc",
		".f4v",
		".flv",
		".m2v",
		".m4p",
		".m4v",
		".mkv",
		".mng",
		".mov",
		".mp2",
		".mp4",
		".mpe",
		".mpeg",
		".mpg",
		".mpv",
		".mxf",
		".nsv",
		".ogg",
		".ogv",
		".qt",
		".rm",
		".rmvb",
		".roq",
		".srt",
		".svi",
		".vob",
		".webm",
		".wmv",
		".yuv",
	];
	return videoExtensions.some((ext) => fileName.includes(ext));
}
